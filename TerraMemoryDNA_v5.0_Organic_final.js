      let maxDepth = depth;
      for (const key in obj) {
        if (obj.hasOwnProperty(key)) {
          const currentDepth = this.calculate_depth(obj[key], depth + 1);
          maxDepth = Math.max(maxDepth, currentDepth);
        }
      }
